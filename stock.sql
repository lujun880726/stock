CREATE DEFINER=`root`@`localhost` PROCEDURE `stock_day_stock_vol`(IN `day_time_in` int)
    COMMENT '每个交易日晚上计算与前一个交易日的VOL对比'
BEGIN
	/* 定义变量一 */  
	DECLARE  stock_id_v varchar(6);   -- 遍历的股票ID
	DECLARE  in_day_time INT;   			-- 传入的日期
  DECLARE  last_day_time INT;  			-- 上一个交易日日期
	DECLARE  last_day_Vol INT;  			-- 上一个交易日的成交量
	DECLARE  todayVol INT;      			-- 今天的成交量
	
	/* 定义循环数据一 */  
	DECLARE stocklist CURSOR FOR  
		SELECT stock_id FROM stock WHERE 1;#	所有股票

	/* 传入变量赋值 */ 
	set in_day_time = day_time_in;
	
	OPEN stocklist;  
		 /* 循环执行 */  
		 loop  
			FETCH  stocklist INTO stock_id_v;
				-- 上一交易日的日期		 
		 		set last_day_time = 0;
		 		set last_day_Vol = 0;
				
	 			select max(day_time) into last_day_time  FROM `day_harvest_info` where `stock_id`=stock_id_v  and day_time < in_day_time limit 1;

				if last_day_time then
					select vol into last_day_Vol  FROM `day_harvest_info` where `stock_id`=stock_id_v  and day_time = last_day_time limit 1;
				end if;
				-- 昨天的成交量
				if last_day_Vol THEN
					set todayVol = 0;
					-- 今天的成交量
					 SELECT vol  into todayVol FROM `day_harvest_info` where  `stock_id`=stock_id_v  and `day_time` = in_day_time limit 1; 
						 if (last_day_Vol*2 <todayVol) > 0 then
								  INSERT INTO vol_double(`stock_id`,`last_day_Vol`,`todayVol`,`day_time`) VALUES(stock_id_v,last_day_Vol,todayVol,unix_timestamp()); 
									select 1;
						 end if;
				end if;
		end loop;  
	CLOSE stocklist; 
END
;;
DELIMITER ;
